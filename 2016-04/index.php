                <p>프랑스에서는 정부의 노동 개악에 맞서 한달 넘게 학생과 노동자들의 파업이 이어지고 있습니다. 미국에서는 ‘민주적 사회주의자’를 자처하는 버니 샌더스가 민주당 대선 후보 경선에서 돌풍을 일으키고 있습니다. 영국에서는 보수당 정부의 긴축 정책에 반대하는 15만명의 시위가 벌어졌습니다. 세계적 경제위기 속에 변화를 바라는 많은 이들의 투쟁이 곳곳에서 벌어지고 있습니다.</p>
                <p>지난 20대 총선 결과는 한국에서도 변화의 열망이 크게 존재한다는 것을 보여줬습니다. 새누리당이 완패하는 동시에 진보·좌파 후보들은 거대 야당들의 압박 등 어려운 조건에서도 의미있는 진전을 이뤘습니다. 특히 민주노총 전략후보들 중 노회찬(창원 성산), 윤종오(울산 북구), 김종훈(울산 동구) 당선 소식은 우리를 더욱 기쁘게 했습니다. 당선자들이 앞으로의 노동자투쟁에서 우렁찬 스피커가 되기를 기원합니다.</p>
                <p>박근혜 정부의 선거 패배 속에서 열린 4.16 세월호 참사 2주기 추모문화제는 폭우 속에서도 1만 2천여명이 광화문광장을 가득 메웠습니다. 참가자들은 “기억하자! 행동하자!”를 함께 외치며 싸움의 의지를 다시 한 번 다졌습니다. 추모문화제의 제목인 “내일도 4월 16일입니다”처럼 노동자연대 또한 진실이 규명될 때까지 투쟁에 연대해갈 것입니다.</p>
